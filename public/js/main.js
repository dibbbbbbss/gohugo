(() => {
  // <stdin>
  console.log("This site was generated by Hugo.");
  document.addEventListener("DOMContentLoaded", () => {
    console.log("DOM fully loaded and parsed");
    const tabButtons = document.querySelectorAll(".tab_button");
    const tabContents = document.querySelectorAll(".tab-content");
    if (tabButtons.length === 0 || tabContents.length === 0) {
      console.log("Elements not found");
      return;
    }
    tabButtons.forEach((button) => {
      button.addEventListener("click", () => {
        console.log("Button clicked: ", button.getAttribute("data-tab"));
        const targetTab = button.getAttribute("data-tab");
        tabButtons.forEach((btn) => {
          btn.classList.remove("active");
        });
        tabContents.forEach((content) => {
          content.classList.remove("active");
        });
        button.classList.add("active");
        document.getElementById(targetTab).classList.add("active");
      });
    });
  });
  document.addEventListener("DOMContentLoaded", () => {
    const accordionHeaders = document.querySelectorAll(".accordion-header");
    accordionHeaders.forEach((header) => {
      header.addEventListener("click", () => {
        const content = header.nextElementSibling;
        if (content.style.maxHeight) {
          content.style.maxHeight = null;
          content.style.padding = "0 15px";
        } else {
          document.querySelectorAll(".accordion-content").forEach((c) => {
            c.style.maxHeight = null;
            c.style.padding = "0 15px";
          });
          content.style.maxHeight = content.scrollHeight + "px";
          content.style.padding = "15px";
        }
      });
    });
  });
  document.addEventListener("DOMContentLoaded", function() {
    var dropdowns = document.querySelectorAll(".dropdown-toggle");
    dropdowns.forEach(function(dropdown) {
      dropdown.addEventListener("click", function(e) {
        e.preventDefault();
        var dropdownMenu = this.nextElementSibling;
        dropdownMenu.style.display = dropdownMenu.style.display === "block" ? "none" : "block";
      });
    });
    window.addEventListener("click", function(e) {
      dropdowns.forEach(function(dropdown) {
        var dropdownMenu = dropdown.nextElementSibling;
        if (dropdownMenu.style.display === "block" && !dropdownMenu.contains(e.target) && !dropdown.contains(e.target)) {
          dropdownMenu.style.display = "none";
        }
      });
    });
  });
  document.addEventListener("DOMContentLoaded", function() {
    const scrollContainer = document.getElementById("slider-container");
    const halfwayScroll = (scrollContainer.scrollWidth - scrollContainer.clientWidth) / 2;
    scrollContainer.scrollLeft = halfwayScroll;
    let isDown = false;
    let startX;
    let scrollLeft;
    scrollContainer.addEventListener("mousedown", (e) => {
      isDown = true;
      scrollContainer.classList.add("active");
      startX = e.pageX - scrollContainer.offsetLeft;
      scrollLeft = scrollContainer.scrollLeft;
      scrollContainer.style.cursor = "grabbing";
    });
    scrollContainer.addEventListener("mouseleave", () => {
      isDown = false;
      scrollContainer.classList.remove("active");
      scrollContainer.style.cursor = "grab";
    });
    scrollContainer.addEventListener("mouseup", () => {
      isDown = false;
      scrollContainer.classList.remove("active");
      scrollContainer.style.cursor = "grab";
    });
    scrollContainer.addEventListener("mousemove", (e) => {
      if (!isDown)
        return;
      e.preventDefault();
      const x = e.pageX - scrollContainer.offsetLeft;
      const walk = (x - startX) * 2;
      scrollContainer.scrollLeft = scrollLeft - walk;
    });
  });
})();
